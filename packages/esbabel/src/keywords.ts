function invertMap(map: Map<string, string>): Map<string, string> {
  const invertedMap = new Map<string, string>()
  for (const [key, value] of map.entries()) {
    invertedMap.set(value, key)
  }

  return invertedMap
}

export function getDictionary(reverse = false): Map<string, string> {
  if (reverse) {
    return invertMap(keywords)
  }

  return keywords
}
